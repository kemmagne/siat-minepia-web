package org.guce.siat.web.reports.vo;




/**
 * The Class FileItemVo.
 */
public class AsMinaderFileItemVo extends AbstractFileItemVo
{

	/** The trade name. */
	private String tradeName;

	/** The active substance content. */
	private String activeSubstanceContent;

	/** The formulation type. */
	private String formulationType;

	/** The use. */
	private String use;

	/** The origin country. */
	private String originCountry;

	/**
	 * Gets the trade name.
	 *
	 * @return the trade name
	 */
	public String getTradeName()
	{
		return tradeName;
	}

	/**
	 * Gets the active substance content.
	 *
	 * @return the active substance content
	 */
	public String getActiveSubstanceContent()
	{
		return activeSubstanceContent;
	}

	/**
	 * Gets the formulation type.
	 *
	 * @return the formulation type
	 */
	public String getFormulationType()
	{
		return formulationType;
	}

	/**
	 * Gets the use.
	 *
	 * @return the use
	 */
	public String getUse()
	{
		return use;
	}

	/**
	 * Gets the origin country.
	 *
	 * @return the origin country
	 */
	public String getOriginCountry()
	{
		return originCountry;
	}

	/**
	 * Sets the trade name.
	 *
	 * @param tradeName
	 *           the new trade name
	 */
	public void setTradeName(final String tradeName)
	{
		this.tradeName = tradeName;
	}

	/**
	 * Sets the active substance content.
	 *
	 * @param activeSubstanceContent
	 *           the new active substance content
	 */
	public void setActiveSubstanceContent(final String activeSubstanceContent)
	{
		this.activeSubstanceContent = activeSubstanceContent;
	}

	/**
	 * Sets the formulation type.
	 *
	 * @param formulationType
	 *           the new formulation type
	 */
	public void setFormulationType(final String formulationType)
	{
		this.formulationType = formulationType;
	}

	/**
	 * Sets the use.
	 *
	 * @param use
	 *           the new use
	 */
	public void setUse(final String use)
	{
		this.use = use;
	}

	/**
	 * Sets the origin country.
	 *
	 * @param originCountry
	 *           the new origin country
	 */
	public void setOriginCountry(final String originCountry)
	{
		this.originCountry = originCountry;
	}

}
