public class CtCctSeizureVo {
	private String referenceNumber;
	private String managerName;
	private String lotName;
	private String storagePlace;
	private String customerName;
	private String billOfLoading;
	private String origin;
	private String meansOfTransport;
	private String storeKeeper;
	private String customsAgent;

	public CtCctSeizureVo() {
	}

	public String getReferenceNumber() {
		return referenceNumber;
	}

	public void setReferenceNumber(String referenceNumber) {
		this.referenceNumber = referenceNumber;
	}

	public String getManagerName() {
		return managerName;
	}

	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}

	public String getLotName() {
		return lotName;
	}

	public void setLotName(String lotName) {
		this.lotName = lotName;
	}

	public String getStoragePlace() {
		return storagePlace;
	}

	public void setStoragePlace(String storagePlace) {
		this.storagePlace = storagePlace;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getBillOfLoading() {
		return billOfLoading;
	}

	public void setBillOfLoading(String billOfLoading) {
		this.billOfLoading = billOfLoading;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getMeansOfTransport() {
		return meansOfTransport;
	}

	public void setMeansOfTransport(String meansOfTransport) {
		this.meansOfTransport = meansOfTransport;
	}

	public String getStoreKeeper() {
		return storeKeeper;
	}

	public void setStoreKeeper(String storeKeeper) {
		this.storeKeeper = storeKeeper;
	}

	public String getCustomsAgent() {
		return customsAgent;
	}

	public void setCustomsAgent(String customsAgent) {
		this.customsAgent = customsAgent;
	}
	
	
}
